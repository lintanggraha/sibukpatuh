const frameworksWithoutCoverage = ['cobit', 'pbi', 'resilience', 'padg', 'owasp_asvs'];
frameworksWithoutCoverage.forEach(fwId => {
  gapData.coverage[fwId] = [];
  gapData.controls.forEach((ctrl, index) => {
    let status = 'C';
    if (fwId.includes('owasp')) {
      status = (index > 10 && index < 20) ? 'N' : 'P';
      if (ctrl.id === 'req-04' || ctrl.id === 'req-05' || ctrl.id === 'req-09' || ctrl.id === 'req-21') status = 'C';
    } else {
      status = (index % 4 === 0) ? 'P' : 'C'; 
      if (index % 7 === 0) status = 'N';
    }
    
    gapData.coverage[fwId].push({
      id: ctrl.id,
      status: status,
      ref: `CTL-${fwId.toUpperCase()}-${index+1}`,
      note: status === 'N' ? 'Tidak diatur spesifik' : `Kontrol terkait ${ctrl.name}`,
      partialReason: status === 'P' ? `Sistem tata kelola ${fwId.toUpperCase()} hanya menutupi sebagian dari aspek yang dipersyaratkan oleh baseline pada kontrol ini.` : undefined
    });
  });
});
